package java.examp;

public class TotemPole {
	
	private String rulers;
	private String wars;
	private String peasanty;
	
	public String getRulers() {
		return rulers;
	}
	public void setRulers(String rulers) {
		this.rulers = rulers;
	}
	public String getWars() {
		return wars;
	}
	public void setWars(String wars) {
		this.wars = wars;
	}
	public String getPeasanty() {
		return peasanty;
	}
	public void setPeasanty(String peasanty) {
		this.peasanty = peasanty;
	}
	
	

}
